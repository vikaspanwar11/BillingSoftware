    void getAllItems()
    {
        String item_id, item_name;
        int srno=0;
        try
        {
            Connection con=dbcon.DbConnection.getConnection();
            
            PreparedStatement ps=con.prepareStatement("select * from items");
            ResultSet rs=ps.executeQuery();
            while(rs.next())
            {
                srno=srno+1;
                item_id=rs.getString("item_id");
                item_name=rs.getString("item_name");

                Object[] o={srno, item_id, item_name};
                dtm.addRow(o);
            }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }

