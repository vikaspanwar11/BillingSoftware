    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        
        int row=jTable1.getSelectedRow();
        selected_item_id=(String)jTable1.getValueAt(row, 1);
        
        String item_name, item_price, desc;
        
        try
        {
            Connection con=dbcon.DbConnection.getConnection();
            
            PreparedStatement ps=con.prepareStatement("select * from items where item_id=?");
            ps.setString(1, selected_item_id);
            ResultSet rs=ps.executeQuery();
            while(rs.next())
            {
                item_name=rs.getString("item_name");
                item_price=rs.getString("item_price");
                desc=rs.getString("item_description");
                
                jTextField1.setText(item_name);
                jTextField2.setText(item_price);
                jTextArea1.setText(desc);
                
                is=rs.getBinaryStream("item_img");
                
                BufferedImage img = null;
                Image dimg = null;
                try 
                {
                    img = ImageIO.read(is);
                    dimg = img.getScaledInstance(jLabel6.getWidth(), jLabel6.getHeight(),Image.SCALE_SMOOTH);
                    ImageIcon imgThisImg = new ImageIcon(dimg);
                    jLabel6.setIcon(imgThisImg);
                } 
                catch (IOException e) 
                {
                    e.printStackTrace();
                }
            }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        try
        {
            Connection con=dbcon.DbConnection.getConnection();
            
            PreparedStatement ps=con.prepareStatement("delete from items where item_id='"+selected_item_id+"'");
            int i=ps.executeUpdate();
            if(i>0)
            {
                JOptionPane.showMessageDialog(rootPane, "Item deleted successfully");
                
                EditProduct ep=new EditProduct();
                ep.setVisible(true);
                setVisible(false);
            }
            else
            {
                JOptionPane.showMessageDialog(rootPane, "Item not deleted due to some error","Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String item_name=jTextField1.getText();
        String item_price=jTextField2.getText();
        String item_desc=jTextArea1.getText();
        
        if(file!=null)
        {
            try
            {
                Connection con=dbcon.DbConnection.getConnection();

                PreparedStatement ps=con.prepareStatement("update items set item_name=?, item_price=?, item_description=?, item_img=? where item_id=?");
                ps.setString(1, item_name);
                ps.setString(2, item_price);
                ps.setString(3, item_desc);
                ps.setBinaryStream(4, new FileInputStream(file));
                ps.setString(5, selected_item_id);
                
                int i=ps.executeUpdate();
                if(i>0)
                {
                    JOptionPane.showMessageDialog(rootPane, "Item updated successfully");
                    EditProduct ep=new EditProduct();
                    ep.setVisible(true);
                    setVisible(false);
                }
                else
                {
                    JOptionPane.showMessageDialog(rootPane, "Item not updated due to some error","Error", JOptionPane.ERROR_MESSAGE);
                }
            }
            catch(Exception e)
            {
                e.printStackTrace();
            }
        }
        else
        {
            try
            {
                Connection con=dbcon.DbConnection.getConnection();

                PreparedStatement ps=con.prepareStatement("update items set item_name=?, item_price=?, item_description=? where item_id=?");
                ps.setString(1, item_name);
                ps.setString(2, item_price);
                ps.setString(3, item_desc);
                ps.setString(4, selected_item_id);
                
                int i=ps.executeUpdate();
                if(i>0)
                {
                    JOptionPane.showMessageDialog(rootPane, "Item updated successfully");
                    EditProduct ep=new EditProduct();
                    ep.setVisible(true);
                    setVisible(false);
                }
                else
                {
                    JOptionPane.showMessageDialog(rootPane, "Item not updated due to some error","Error", JOptionPane.ERROR_MESSAGE);
                }
            }
            catch(Exception e)
            {
                e.printStackTrace();
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

