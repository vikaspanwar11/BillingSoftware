    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String new_quantity=(String)jComboBox1.getSelectedItem();
        int new_total_price=getItemPrice();
        
        ArrayList al=(ArrayList) hm.get(selected_itemid);
        al.set(2, new_quantity);
        al.set(3, String.valueOf(new_total_price));
        
        hm.replace(selected_itemid, al);
        
        JOptionPane.showMessageDialog(rootPane, "Item updated successfully");
        
        AddToCartDetails ad=new AddToCartDetails(ed, hm);
        ad.setVisible(true);
        setVisible(false);
        
        printAllHmValues();
        
    }//GEN-LAST:event_jButton1ActionPerformed

