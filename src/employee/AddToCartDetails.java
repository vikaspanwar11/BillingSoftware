    int getItemPrice()
    {
        String item_price=(String)jTable1.getValueAt(row, 3);
        String new_quantity=(String)jComboBox1.getSelectedItem();
        int new_total_price=Integer.parseInt(item_price) * Integer.parseInt(new_quantity);
        return new_total_price;
    }
    
