    void printAllItemDetails(HashMap<String, ArrayList> hm)
    {
        int srno=0;
        for(Map.Entry me:hm.entrySet())
        {
            srno=srno+1;
            ArrayList al=(ArrayList)me.getValue();
            Object[] obj={srno, me.getKey(), al.get(0), al.get(1), al.get(2), al.get(3)};
            dtm.addRow(obj);
            
            total_bill=total_bill+Integer.parseInt((String)al.get(3));
        }
        jLabel7.setText(String.valueOf(total_bill));
        
        printAllHmValues();
    }
    
    void printAllHmValues()
    {
        for(Map.Entry me:hm.entrySet())
        {
            System.out.println(me.getKey()+" : "+me.getValue());
        }
        System.out.println("========================================");
    }

